    string LineOfToss(int heads, out string zhi, out string cuo)
    {
        // heads = 正面数量: 3=老阳(阳○→阴) 2=少阳 1=少阴 0=老阴(阴○→阳)
        switch (heads) {
            case 3: zhi = "阴"; cuo = "阴"; return "阳○";
            case 2: zhi = "阳"; cuo = "阴"; return "阳";
            case 1: zhi = "阴"; cuo = "阳"; return "阴";
            default: zhi = "阳"; cuo = "阳"; return "阴○";
        }
    }

