    void SaveHistories()
    {
        try {
            Directory.CreateDirectory(Path.GetDirectoryName(HistoryPath));
            var lines = new List<string>();
            for (int i = 0; i < histories.Length; i++) {
                foreach (string item in histories[i]) {
                    string data = Convert.ToBase64String(Encoding.UTF8.GetBytes(item));
                    lines.Add(i.ToString() + "|" + data);
                }
            }
            File.WriteAllLines(HistoryPath, lines.ToArray(), Encoding.UTF8);
        } catch { }
    }

    void LoadHistories()
    {
        try {
            if (!File.Exists(HistoryPath)) return;
            string[] lines = File.ReadAllLines(HistoryPath, Encoding.UTF8);
            foreach (string line in lines) {
                int pos = line.IndexOf('|');
                if (pos <= 0) continue;
                int idx;
                if (!int.TryParse(line.Substring(0, pos), out idx)) continue;
                if (idx < 0 || idx >= histories.Length) continue;
                string item = Encoding.UTF8.GetString(Convert.FromBase64String(line.Substring(pos + 1)));
                histories[idx].Add(item);
                if (histories[idx].Count > 30) histories[idx].RemoveAt(0);
            }
        } catch { }
    }

