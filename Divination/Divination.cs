    static string Cjk(string s) // 提取第一段连续汉字
    {
        int st = -1;
        for (int i = 0; i < s.Length; i++) {
            bool isCjk = s[i] >= 0x4E00 && s[i] <= 0x9FA5;
            if (isCjk && st < 0) st = i;
            if (!isCjk && st >= 0) return s.Substring(st, i - st);
        }
        return st >= 0 ? s.Substring(st) : s;
    }

