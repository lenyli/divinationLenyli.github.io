    string TimeStamp()
    {
        DateTime t = DateTime.Now;
        return t.ToString("yyyy-MM-dd ") + (t.Hour < 12 ? "上午" : "下午") + t.ToString("HH") + "：" + t.ToString("mm");
    }

    void AddHistory() { AddHistoryText(copyText); }

    void AddHistoryText(string text)
    {
        var h = histories[curModule];
        h.Add(TimeStamp() + "  " + text);
        if (h.Count > 30) { h.RemoveAt(0); if (sessGen > 0) sessGen--; if (sessMaj > 0) sessMaj--; }
        SaveHistories();
    }

    // ================= 清空 / 历史 =================
    // 页索引：0综合 1日期 2六爻 3塔罗通用 4YESNO 5大牌 6雷诺曼 7卢恩 8占星 9灵签
    int PageIndex()
    {
        if (curModule == 0) return curHomeTab;
        if (curModule == 1) return 2;
        if (curModule == 2) return 3 + curTab;
        return 6 + (curModule - 3);
    }

    void SaveState()
    {
        int p = PageIndex();
        pageRtf[p] = txtOut.Rtf; pageCopy[p] = copyText;
    }

    void RestoreState()
    {
        int p = PageIndex();
        if (pageRtf[p] == null) { txtOut.Clear(); copyText = ""; }
        else { txtOut.Rtf = pageRtf[p]; copyText = pageCopy[p]; }
    }

    void OnClear(object sender, EventArgs e)
    {
        txtQ.Clear(); txtOut.Clear(); copyText = "";
        drawnGen.Clear(); drawnMajor.Clear();
        sessGen = -1; sessMaj = -1;
        int p = PageIndex();
        pageRtf[p] = null; pageCopy[p] = null;
    }

    void OnHistory(object sender, EventArgs e)
    {
        List<string> h = histories[curModule];
        int mod = curModule;
        var f = new Form();
        f.Text = "历史记录 - " + modBtns[curModule].Text + "（最近30条）";
        f.Font = Font;
        f.ClientSize = new Size(620, 470);
        f.StartPosition = FormStartPosition.CenterParent;
        var rt = new RichTextBox();
        rt.ReadOnly = true; rt.ScrollBars = RichTextBoxScrollBars.Vertical;
        rt.BackColor = SystemColors.Window;
        rt.SetBounds(10, 10, 600, 408);
        rt.Anchor = AnchorStyles.Top | AnchorStyles.Bottom | AnchorStyles.Left | AnchorStyles.Right;
        f.Controls.Add(rt);
        Action fill = delegate() {
            rt.Clear();
            string nl = Environment.NewLine;
            for (int i = 0; i < h.Count; i++) {
                int sep = h[i].IndexOf("  ");
                rt.SelectionStart = rt.TextLength;
                rt.SelectionFont = new Font(rt.Font, FontStyle.Bold);
                rt.AppendText(sep > 0 ? h[i].Substring(0, sep) : "");
                rt.SelectionStart = rt.TextLength;
                rt.SelectionFont = new Font(rt.Font, FontStyle.Regular);
                rt.AppendText((sep > 0 ? h[i].Substring(sep) : h[i]) + (i < h.Count - 1 ? nl + nl : ""));
            }
        };
        fill();
        var bCopy = new Button(); bCopy.Text = "复制全部"; bCopy.SetBounds(10, 426, 100, 34);
        bCopy.Anchor = AnchorStyles.Bottom | AnchorStyles.Left;
        bCopy.Click += delegate(object s2, EventArgs e2) {
            if (h.Count > 0) { Clipboard.SetText(string.Join(Environment.NewLine + Environment.NewLine, h)); ShowToast("已复制"); }
        };
        f.Controls.Add(bCopy);
        var bClr = new Button(); bClr.Text = "清除全部"; bClr.SetBounds(122, 426, 100, 34);
        bClr.Anchor = AnchorStyles.Bottom | AnchorStyles.Left;
        bClr.Click += delegate(object s2, EventArgs e2) {
            h.Clear(); rt.Clear();
            if (mod == 2) { sessGen = -1; sessMaj = -1; }
            SaveHistories();
        };
        f.Controls.Add(bClr);
        f.Show(this);
    }

