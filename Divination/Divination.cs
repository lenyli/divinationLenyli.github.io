    void Append(string s, FontStyle style)
    {
        txtOut.SelectionStart = txtOut.TextLength;
        txtOut.SelectionFont = new Font(txtOut.Font, style);
        txtOut.AppendText(s);
    }

    void OnDivine(object sender, EventArgs e)
    {
        string q = txtQ.Text.Trim();
        if (q.Length == 0) q = "（未填写问题）";
        DoDivine(q);
        SaveState();
    }

    void DoDivine(string q)
    {
        if (curModule == 0) { if (curHomeTab == 0) DivineHome(q); else DivineDate(q); return; }
        if (curModule == 6) { DivineQian(q); return; }
        if (curModule == 2) { DivineTarot(q); return; }
        var lines = new List<string[]>();
        string result;
        if (curModule == 1) result = DivineLiuYao(lines);
        else if (curModule == 3) result = DivineLenormand(lines);
        else if (curModule == 4) result = DivineRunes(lines);
        else result = DivineAstro(lines);
        copyText = q + "：" + result;
        string nl = Environment.NewLine;
        txtOut.Clear();
        AddHistory();
        Append(copyText + nl + nl + "―― 简要说明 ――" + nl, FontStyle.Regular);
        for (int i = 0; i < lines.Count; i++) {
            Append(lines[i][0], FontStyle.Regular);
            Append(lines[i][1], FontStyle.Bold);
            Append("：" + lines[i][2] + "。", FontStyle.Regular);
            if (lines[i][3].Length > 0) Append(lines[i][3], FontStyle.Italic);
            if (i < lines.Count - 1) Append(nl, FontStyle.Regular);
        }
    }

    void OnCopy(object sender, EventArgs e)
    {
        if (copyText.Length == 0) return;
        Clipboard.SetText(copyText);
        ShowToast("已复制");
    }

    void ShowToast(string msg)
    {
        var f = new Form();
        f.FormBorderStyle = FormBorderStyle.None;
        f.StartPosition = FormStartPosition.Manual;
        f.ShowInTaskbar = false;
        f.TopMost = true;
        f.BackColor = Color.FromArgb(60, 60, 60);
        var lbl = new Label();
        lbl.Text = msg; lbl.ForeColor = Color.White; lbl.BackColor = Color.Transparent;
        lbl.Font = new Font(Font.FontFamily, 11f, FontStyle.Bold);
        lbl.AutoSize = true; lbl.Location = new Point(20, 12);
        f.Controls.Add(lbl);
        f.ClientSize = new Size(lbl.PreferredWidth + 40, lbl.PreferredHeight + 24);
        var owner = ActiveForm ?? this;
        f.Location = new Point(owner.Location.X + (owner.Width - f.Width) / 2,
                               owner.Location.Y + (owner.Height - f.Height) / 2);
        var t = new Timer(); t.Interval = 1200;
        t.Tick += delegate(object s2, EventArgs e2) { t.Stop(); t.Dispose(); f.Close(); f.Dispose(); };
        f.Show(this);
        t.Start();
    }

