entries = {}
with open(SRC, encoding="utf-8") as f:
    for line in f:
        m = re.match(r"\s*(\d+)\.\s*(.+)", line)
        if not m:
            continue
        num = int(m.group(1))
        phrases = re.split(r"[\s\u3000]+", m.group(2).strip())
        phrases = [p for p in phrases if p]
        entries[num] = phrases

missing = [n for n in range(1, 397) if n not in entries]
if missing:
    raise SystemExit(f"缺少编号: {missing}")
